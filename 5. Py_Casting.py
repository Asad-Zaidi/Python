# integers
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3")

print (x,y,z)

# Float

x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2

print (x,y,z,w)

# String
x = str("s1") # x will be 's1'
y = str(2)    # y will be '2'
z = str(3.0)  # z will be '3.0'

print (x,y,z)

# Complex numbers
x = complex(1)     # x is (1+0j)
y = complex(2, 3)  # y is (2+3j)

print (x,y)
